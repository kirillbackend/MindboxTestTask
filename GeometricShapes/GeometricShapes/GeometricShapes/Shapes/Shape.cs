
namespace GeometricShapes
{
    /// <summary>
    /// Класс Shape является базовым классом для всех фигур.
    /// </summary>
    public abstract class Shape
    {
        /// <summary>
        /// Метод Area считает площадь фигуры
        /// </summary>
        /// <returns> плодаль фигуры в double </returns>
        public abstract double Area();
    }
}
